var elem = document.getElementById("myBar");
var width = 0;

function myFunction1() {
    if(document.getElementById("Intrebare1").style.color == "green") {
        document.getElementById("Intrebare1").innerHTML = "Da";
        document.getElementById("Intrebare1").style.color = "black";
        if (width > 0) {
            width = width - 20;
            elem.style.width = width + "%";
            elem.innerHTML = width + "%";
        }
    } else {
        if (width < 100) {
            width = width + 20;
            elem.style.width = width + "%";
            elem.innerHTML = width + "%";
        }
        document.getElementById("Intrebare1").innerHTML = "DA";
        document.getElementById("Intrebare1").style.color = "green";
    }
};

